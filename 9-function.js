// 1st function
function add(a, b) {
  return a + b;
}
const sum = add(3, 5);
console.log(sum); // Output: 8

// 2nd function
function greet(name) {
  return `Hello, ${name}!`;
}
const greeting = greet("Alice");
console.log(greeting); // Output: Hello, Alice!

// 3rd function
function multiply(x, y) {
  return x * y;
}
const result = multiply(4, 6);
console.log(result); // Output: 24

// 4th function
function square(number) {
  return number * number;
}
const squared = square(3);
console.log(squared); // Output: 9

// 5th function
function isEven(number) {
  return number % 2 === 0;
}
const isEvenResult = isEven(7);
console.log(isEvenResult); // Output: false
