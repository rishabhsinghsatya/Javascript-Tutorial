// 1. Arithmetic Operators:

// Addition +:

let result1 = 5 + 3; // result will be 8
console.log(result1);

// Subtraction -:
let result2 = 10 - 4; // result will be 6
console.log(result2);

// Multiplication *:
let result3 = 6 * 2; // result will be 12
console.log(result3);

// Division /:
let result4 = 20 / 4; // result will be 5
console.log(result4);

// Modulus % (remainder):
let result = 15 % 7; // result will be 1
console.log(result);

//2. Assignment Operators:

// Assignment =:
let x = 10;
console.log(x);

// Addition assignment +=:
let y = 5;
y += 3; // y will be 8
console.log(y);

//3. Comparison Operators:

// Equal == (loose equality):
let isEqual = 5 == "5"; // isEqual will be true
console.log(isEqual);

// Strict equal === (strict equality):
let isStrictEqual = 5 === "5"; // isStrictEqual will be false
console.log(isStrictEqual);

// Not equal != (loose inequality):
let isNotEqual = 10 != "5"; // isNotEqual will be true
console.log(isNotEqual);

// Greater than >:
let isGreaterThan = 10 > 5; // isGreaterThan will be true
console.log(isGreaterThan);

// Less than <:
let isLessThan = 3 < 1; // isLessThan will be false
console.log(isLessThan);

//4. Logical Operators:

// AND &&:
let isTrue1 = true && true; // isTrue will be true
console.log(isTrue1);

// OR ||:
let isTrue = true || false; // isTrue will be true
console.log(isTrue);

// NOT !:
let isFalse = !true; // isFalse will be false
console.log(isFalse);

//5. Unary Operators:

// Increment ++:
let count1 = 5;
count++; // count will be 6
console.log(count1);

// Decrement --:
let count = 5;
count--; // count will be 4
console.log(count);
